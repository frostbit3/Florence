package com.florence.model.content.combat;

public class CombatHit {

    private CombatType type;
    private int damage;

    public CombatHit(CombatType type, int damage) {
        this.type = type;
        this.damage = damage;
    }

    public CombatType getType() {
        return type;
    }

    public void setType(CombatType type) {
        this.type = type;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }
}
